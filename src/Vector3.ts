class Vector3 {
  public _x = 0;
  get x() {
    return this._x;
  }
  set x(value) {
    this._x = value;
  }

  public _y = 0;
  get y() {
    return this._y;
  }
  set y(value) {
    this._y = value;
  }

  public _z = 0;
  get z() {
    return this._z;
  }
  set z(value) {
    this._z = value;
  }
}

export default Vector3;
